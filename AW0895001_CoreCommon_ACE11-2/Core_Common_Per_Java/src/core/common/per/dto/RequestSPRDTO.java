package core.common.per.dto;

public class RequestSPRDTO {
	
	//Datos Request
	private String codigoIdioma;
	private String codigoCanal;
	private String codigoProveedorServicio;
	private String codigoRespuestaProveedor;
	private String estadoRespuesta;
	
	public String getCodigoIdioma() {
		return codigoIdioma;
	}
	public void setCodigoIdioma(String codigoIdioma) {
		this.codigoIdioma = codigoIdioma;
	}
	public String getCodigoCanal() {
		return codigoCanal;
	}
	public void setCodigoCanal(String codigoCanal) {
		this.codigoCanal = codigoCanal;
	}
	public String getCodigoProveedorServicio() {
		return codigoProveedorServicio;
	}
	public void setCodigoProveedorServicio(String codigoProveedorServicio) {
		this.codigoProveedorServicio = codigoProveedorServicio;
	}
	public String getCodigoRespuestaProveedor() {
		return codigoRespuestaProveedor;
	}
	public void setCodigoRespuestaProveedor(String codigoRespuestaProveedor) {
		this.codigoRespuestaProveedor = codigoRespuestaProveedor;
	}
	public String getEstadoRespuesta() {
		return estadoRespuesta;
	}
	public void setEstadoRespuesta(String estadoRespuesta) {
		this.estadoRespuesta = estadoRespuesta;
	}
	
}
